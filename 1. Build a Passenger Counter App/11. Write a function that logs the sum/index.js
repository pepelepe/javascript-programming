
// Create a function that logs out the sum of all the lap times

function allLaps(lap1, lap2, lap3) {
    console.log(`The number of all laps is: ${lap1}, ${lap2}, ${lap3}`);
}

allLaps(45, 293, 42);

function totalLapTime(lap1, lap2, lap3) {
    console.log(`The total time of all laps is: ${lap1 + lap2 + lap3}`);
}

totalLapTime(628, 47.368, 389);